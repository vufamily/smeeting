/**
 * Meeting Assistant - Results Manager
 * Handles loading and displaying meeting results
 */

class ResultsManager {
    constructor(meetingId) {
        this.meetingId = meetingId;
        this.data = null;
        
        // Callbacks
        this.onResultsLoaded = null;
        this.onError = null;
    }

    /**
     * Load results from API
     */
    async loadResults(meetingId = this.meetingId) {
        try {
            const response = await fetch(`/api/result/${meetingId}`);
            
            if (!response.ok) {
                const error = await response.json();
                throw new Error(error.error || 'Failed to load results');
            }

            this.data = await response.json();

            if (this.onResultsLoaded) {
                this.onResultsLoaded(this.data);
            }

            return this.data;
        } catch (error) {
            if (this.onError) {
                this.onError(error.message);
            }
            throw error;
        }
    }

    /**
     * Get raw results data
     */
    getResultsData() {
        return this.data;
    }

    /**
     * Get formatted transcript for export
     */
    getTranscriptText() {
        if (!this.data || !this.data.transcription) return '';

        return this.data.transcription.map(seg => {
            const start = formatTime(seg.start);
            const end = formatTime(seg.end);
            return `[${start} - ${end}] ${seg.speaker}: ${seg.text}`;
        }).join('\n\n');
    }

    /**
     * Get formatted decisions for export
     */
    getDecisionsText() {
        if (!this.data || !this.data.decisions) return '';

        return this.data.decisions.map(d => {
            return `• ${d.text} (${d.speaker} at ${formatTime(d.timestamp)})`;
        }).join('\n');
    }

    /**
     * Get formatted tasks for export
     */
    getTasksText() {
        if (!this.data || !this.data.tasks) return '';

        return this.data.tasks.map(t => {
            const check = t.completed ? '☑' : '☐';
            const due = t.due_date ? ` (due: ${t.due_date})` : '';
            const assignee = t.assignee ? ` [${t.assignee}]` : '';
            return `${check} ${t.text}${assignee}${due}`;
        }).join('\n');
    }

    /**
     * Generate full export text
     */
    generateExportText() {
        if (!this.data) return '';

        let text = `Meeting: ${this.data.meeting_name || 'Untitled Meeting'}\n`;
        text += `Date: ${formatDate(this.data.created_at)}\n`;
        text += `Duration: ~${Math.round(this.data.transcription?.length * 0.5 || 0)} minutes\n\n`;

        text += '═══════════════════════════════════════\n';
        text += 'TRANSCRIPT\n';
        text += '═══════════════════════════════════════\n\n';
        text += this.getTranscriptText();

        text += '\n\n═══════════════════════════════════════\n';
        text += 'KEY DECISIONS\n';
        text += '═══════════════════════════════════════\n\n';
        text += this.getDecisionsText();

        text += '\n\n═══════════════════════════════════════\n';
        text += 'ACTION ITEMS\n';
        text += '═══════════════════════════════════════\n\n';
        text += this.getTasksText();

        text += '\n\n═══════════════════════════════════════\n';
        text += 'MEETING SUMMARY\n';
        text += '═══════════════════════════════════════\n\n';
        text += this.data.summary || 'No summary available';

        text += '\n\n───────────────────────────────────────\n';
        text += 'Generated by Meeting Assistant';

        return text;
    }

    /**
     * Generate Markdown export
     */
    generateMarkdown() {
        if (!this.data) return '';

        let md = `# Meeting Notes: ${this.data.meeting_name || 'Untitled Meeting'}\n\n`;
        md += `**Date:** ${formatDate(this.data.created_at)}\n`;
        md += `**Participants:** ${getUniqueSpeakers(this.data.transcription).join(', ')}\n\n`;

        md += '## Transcript\n\n';
        if (this.data.transcription) {
            this.data.transcription.forEach(seg => {
                md += `**${seg.speaker}** [${formatTime(seg.start)}]:\n${seg.text}\n\n`;
            });
        }

        md += '## Key Decisions\n\n';
        if (this.data.decisions) {
            this.data.decisions.forEach(d => {
                md += `- ${d.text} *(${d.speaker}, ${formatTime(d.timestamp)})*\n`;
            });
        }

        md += '\n## Action Items\n\n';
        if (this.data.tasks) {
            this.data.tasks.forEach(t => {
                const check = t.completed ? 'x' : ' ';
                const due = t.due_date ? ` — Due: ${t.due_date}` : '';
                const assignee = t.assignee ? ` **${t.assignee}**` : '';
                md += `- [${check}] ${t.text}${assignee}${due}\n`;
            });
        }

        md += '\n## Summary\n\n';
        md += this.data.summary || 'No summary available.';

        md += '\n\n---\n*Generated by Meeting Assistant*';

        return md;
    }

    /**
     * Export to JSON
     */
    exportJSON() {
        return JSON.stringify(this.data, null, 2);
    }
}

// Utility functions

function formatTime(seconds) {
    if (typeof seconds !== 'number') return '00:00';
    const mins = Math.floor(seconds / 60);
    const secs = Math.floor(seconds % 60);
    return `${mins.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}`;
}

function formatDate(dateStr) {
    if (!dateStr) return '';
    const date = new Date(dateStr);
    return date.toLocaleDateString('en-US', {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    });
}

function getUniqueSpeakers(transcription) {
    if (!transcription) return [];
    const speakers = [...new Set(transcription.map(s => s.speaker))];
    return speakers;
}

function escapeHtml(text) {
    if (!text) return '';
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
}

function downloadFile(content, filename, mimeType) {
    const blob = new Blob([content], { type: mimeType });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = filename;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
}

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { ResultsManager, formatTime, formatDate, escapeHtml };
}